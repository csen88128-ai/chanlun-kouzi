# 获取知识库函数
def get_chanlun_knowledge(key_path: str) -> str:
    """
    根据键路径获取缠论知识

    Args:
        key_path: 键路径，如 "level_definition.时间级别.级别分类"

    Returns:
        知识内容字符串
    """
    keys = key_path.split(".")
    current = CHANLUN_MULTI_LEVEL_KNOWLEDGE

    for key in keys:
        if isinstance(current, dict) and key in current:
            current = current[key]
        else:
            return f"未找到知识: {key_path}"

    return str(current)


def get_current_level_info() -> dict:
    """获取当前系统的级别信息"""
    return CHANLUN_MULTI_LEVEL_KNOWLEDGE["current_system_level_info"]


